function matchPhoneNumber(input) {

    let pattern = /(?:\s|^)\+359( |-)2\1\d{3}\1\d{4}\b/g;
    // Телефонът трябва да започва с празно или да е начало на стринг, която правим non-capturing group -> трябва да има +359 ->
    // трябва да има сепаратор празно или тире - > трябва да има цифрата 2 -> трябва да има сепаратор един от двата посочени ->
    // трябва да има 3 цифри -> трябва да има сепаратор един от двата посочени -> трябва да има 4 цифри 

    let match = pattern.exec(input);        // Изваждаме си първото съвпадение
    let validPhones = [];                   // Правим празен масив, в който да пълним валидните телефонни номера

    while (match !== null) {                // Докато намираме съвпадение, въртим цикъла 
        validPhones.push(match[0].trim());  // Към телефоните добавяме, нулевия индекс, което е телефона, и премахваме излишните празни пространства отзад и отпред 
        match = pattern.exec(input);        // Подменяме стойността на съвпадението
    }

    console.log(validPhones.join(", "));    // Принтираме валидните телефони, разделени със запетая и интервал 
}
matchPhoneNumber("+359 2 222 2222,359-2-222-2222, +359/2/222/2222, +359-2 222 2222 +359 2-222-2222, +359-2-222-222, +359-2-222-22222 +359-2-222-2222");